<template>
  <div>
    <b-container>
      <PsAccounts>
        <template v-slot:body>
          <AccountList />
        </template>
      </PsAccounts>
    </b-container>

    <b-container
      class="mt-4"
      v-if="firebaseStatusAccount !== false && paypalStatusAccount !== false"
    >
      <PaymentAcceptance />
    </b-container>

    <b-container
      class="mt-4"
      v-else
    >
      <Reassurance />
    </b-container>
  </div>
</template>

<script>
  import {PsAccounts} from 'prestashop_accounts_vue_components';
  import AccountList from '@/components/panel/account-list';
  import PaymentAcceptance from '@/components/panel/payment-acceptance';
  import Reassurance from '@/components/block/reassurance';
  import {isOnboardingCompleted} from 'prestashop_accounts_vue_components';

  export default {
    name: 'Accounts',
    components: {
      PsAccounts,
      AccountList,
      PaymentAcceptance,
      Reassurance,
    },
    computed: {
      firebaseStatusAccount() {
        return isOnboardingCompleted();
      },
      paypalStatusAccount() {
        return this.$store.state.paypal.onboardingCompleted;
      },
    },
  };
</script>
