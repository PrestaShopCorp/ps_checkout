<?php

namespace PrestaShop\Module\PrestashopCheckout\PayPal\Order\DTO;

class Link
{
    /**
     * @var string
     */
    private $href;
    /**
     * @var string
     */
    private $rel;
    /**
     * @var string
     */
    private $method;

    /**
     * @return string
     */
    public function getHref()
    {
        return $this->href;
    }

    /**
     * @param string $href
     */
    public function setHref($href)
    {
        $this->href = $href;
    }

    /**
     * @return string
     */
    public function getRel()
    {
        return $this->rel;
    }

    /**
     * @param string $rel
     */
    public function setRel($rel)
    {
        $this->rel = $rel;
    }

    /**
     * @return string
     */
    public function getMethod()
    {
        return $this->method;
    }

    /**
     * @param string $method
     */
    public function setMethod($method)
    {
        $this->method = $method;
    }
}
